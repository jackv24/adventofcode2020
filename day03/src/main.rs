fn get_trees_encountered(input: [[char; 31]; 323], move_r: usize, move_d: usize) -> i32 {
    let mut x = move_r;
    let mut y = move_d;

    let mut trees_encountered = 0;

    let height = input.len();
    for i in 0..height {
        let arr = input[i];

        let width = arr.len();
        for j in 0..width {
            let mut c = arr[j];

            if j == x && i == y {
                x += move_r;
                y += move_d;

                if x >= width {
                    x -= width;
                }
                if y >= height {
                    y -= height;
                }

                if c == '#' {
                    c = 'X';
                    trees_encountered += 1;
                } else {
                    c = '0';
                }
            }

            print!("{}", c);
        }
        println!();
    }

    println!("Encountered {} trees", trees_encountered);

    trees_encountered
}
